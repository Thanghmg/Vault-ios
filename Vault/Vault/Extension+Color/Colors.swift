extension UIColor {
    
    class var Purple: UIColor {
        return UIColor(red: 114/255, green: 101/255, blue: 226/255, alpha: 1)
    }
    
    class var lightPurple: UIColor {
        return UIColor(red: 245/255, green: 239/255, blue: 255/255, alpha: 0.5)
    }
}
